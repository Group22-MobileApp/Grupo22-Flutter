class User {
  final String carrer;
  final String email;
  final String username;
  final String password;
  final String id;
  final String number;
  final String imageUrl;
  final String name;

  User({
    required this.carrer,
    required this.email,
    required this.username,
    required this.password,
    required this.id,
    required this.number,
    required this.imageUrl,
    required this.name,
  });
}
